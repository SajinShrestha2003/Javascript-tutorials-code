let myName = 'Sajin Shrestha'
console.log(myName.length)  //->length of myName

console.log(myName.toUpperCase())   //->built-in string function to print myName in uppercase

console.log(myName.toLowerCase())   //->built-in string function to print myName in lowercase

console.log(myName.slice(2,5))  //->print myName character from '2nd' upto '5th' place

console.log(myName.replace('Sajin', 'Sarita'))  //->to replace the characters in string